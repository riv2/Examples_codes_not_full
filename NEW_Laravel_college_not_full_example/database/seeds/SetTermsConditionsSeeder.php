<div>бағдарламаларды және деректер базаларынкөшірмелеу (жаңғырту), </div>
<div>оларды декомпиляциялау, түрлендіру жолымен пайдалануға, және әрі </div>
<div>қарай таратуға, көпшілікке көрсетуге, жалпының назарына жеткізуге </div>
<div>қатаң тыйым салынған.</div>
