<div> 5. Хабарламалар</div>
<div>5.1. Мирас Пайдаланушыға оның Жеке кабинетте көрсеткен электрондық </div>
<div>адреске, ұялы телефон нөміріне Қолданбаның аясында немесе онымен </div>
<div>байланысты болып жатқан маңызды оқиғалар туралы ақпараттық </div>
<div>электрондық хабарламаларды (бұдан әрі – «нотификаторларды») </div>
<div>жолдауға құқылы.</div>
<div>5.2. Бұдан өзгеПайдаланушыға профилактикалық жұмыстарға, </div>
<div>Пайдаланушының бұзушылықтарына байланысты Сайтқа және/немесе </div>
<div>Сервиске қатынаудың шектеулері, Сайттың функционалының өзгеруі, </div>
<div>Келісімнің, міндетті және қосымша құжаттардың өзгеруін қоса </div>
<div>алғанда, Сервисті ұсынудың мазмұны мен шарттару туралы хабарлауға</div>
<div>арналған нотификаторларды пайдалануға рұқсат етіледі.</div>
<div>5.3. Мирас өз еркімен өзіндік қызметтерінің мен үшінші тұлғалар </div>
<div>өнімдерінің (қызметтерінің) жарнамасын тарату үшін </div>
<div>нотификаторларды пайдалануға құқылы.</div>
<div>6. Жай электрондыққолтаңбаны пайдалану туралы келісім </div>
<div>6.1. Мирас пен Пайдаланушының өзара қарым-қатынастарында жай </div>
<div>электрондық қолтаңбамен куәландырылған электрондық құжаттар </div>
<div>пайдаланылуы мүмкін. </div>
<div>6.2. Жай электрондыққолтаңба болыпПайдаланушының логині мен паролін, </div>
<div>не болмаса Жеке кабинетте көрсетілген электрондық почтасының </div>
<div>адресін, сондай-ақ	</div>		
<div>Пайдаланушыға СМС-хабарлама (SMS) арқылы ұсынылатын СМС-кодты </div>
<div>пайдалану арқылы тікелей Пайдаланушының электрондық қолтаңбасын</div>
<div>қалыптастыру фактісі расталатын электрондыққолтаңба танылады.</div>
<div>6.3. Тараптардың келісіміменжай электрондық қолтаңбамен қол </div>
<div>қойылған электрондыққұжаттарөз қолымен қойылған қағаз </div>
<div>тасығыштардағы құжаттарға мәні бірдей болып танылады.</div>
<div>6.4. Мирас Сервисті және қызметтерді пайдалану жөнінде кез келген </div>
<div>іс-әрекеттерді жасаған жағдайда Пайдаланушының Сайтқа </div>
<div>тіркелген/немесе авторландырылған кезде көрсеткен пайдаланатын </div>
<div>логині мен паролі немесе алынған СМС-коды немесе Мирасқа осы </div>
<div>адрестен хабарламалар түскен жағдайда Пайдаланушы пайдаланатын </div>
<div>электрондық почтаның адресі  бойыншажай электрондық қолтаңба </div>
<div>сәйкес келетін Пайдаланушыны анықтайды.</div>
<div>6.5. Анықталған Пайдаланушының жай электрондық қолтаңбаны </div>
<div>пайдаланып жасаған кез келген іс-әрекеттері осындай Пайдаланушы </div>
<div>жасаған болып есептеледі. </div>
<div>6.6. Пайдаланушының өзінің Жеке кабинетіндегі бағдарламалық </div>
<div>құралдардың көмегімен Сервиске қолжетімділік беруге, Мирастың </div>
<div>Қызметтеріне ақы төлеуге тапсырысын рәсімдеу мұндай электрондық</div>
<div>құжатқа Пайдаланушының жай электрондық қолтаңбамен қол қойғанын</div>
<div>айғақтайды және оның Қызметтер прейскурантында көрсетілген </div>
