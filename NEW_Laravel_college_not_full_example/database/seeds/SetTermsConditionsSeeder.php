<div>бөліктерін немесе оларға қолжетімділікті жаңғыртуға, қайталауға </div>
<div>және көшірмелеуге, сатуға және қандай да бір коммерциялық мақсатта</div>
<div>пайдалануға құқығы жоқ.</div>
